public class Demo4 {
    public static void main(String[] args) {
        /*Data types and literals */
        //floating point numbers
        //decimal
        System.out.println(10.e2);
        System.out.println(10.E2);

        //hexadecimal
        System.out.println(0xFF.p2);
        System.out.println(0xFF.P2);
        System.out.println(10.25e2);
        System.out.println(0xFA.Bp6);
        System.out.println(0xA.Fp2);

    }
}
